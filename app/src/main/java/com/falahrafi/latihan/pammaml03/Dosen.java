package com.falahrafi.latihan.pammaml03;

public class Dosen {
    private String name;
    private String progdi;
    private String jenisKelamin;
    private String jabatanFungsional;
    private String pendidikanTertinggi;
    private String statusIkatanKerja;
    private String statusAktivitas;
    private int photo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getProgdi() {
        return progdi;
    }

    public void setProgdi(String progdi) {
        this.progdi = progdi;
    }

    public String getJenisKelamin() {
        return jenisKelamin;
    }

    public void setJenisKelamin(String jenisKelamin) {
        this.jenisKelamin = jenisKelamin;
    }

    public String getJabatanFungsional() {
        return jabatanFungsional;
    }

    public void setJabatanFungsional(String jabatanFungsional) {
        this.jabatanFungsional = jabatanFungsional;
    }

    public String getPendidikanTertinggi() {
        return pendidikanTertinggi;
    }

    public void setPendidikanTertinggi(String pendidikanTertinggi) {
        this.pendidikanTertinggi = pendidikanTertinggi;
    }

    public String getStatusIkatanKerja() {
        return statusIkatanKerja;
    }

    public void setStatusIkatanKerja(String statusIkatanKerja) {
        this.statusIkatanKerja = statusIkatanKerja;
    }

    public String getStatusAktivitas() {
        return statusAktivitas;
    }

    public void setStatusAktivitas(String statusAktivitas) {
        this.statusAktivitas = statusAktivitas;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }
}
